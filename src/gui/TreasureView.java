    public void setTreasure(Treasure t) {

        treasureModel = t;

        String nameText = "<html>" + treasureModel.getName() + "</html>";
        
        lblName.setText(nameText);
        
        
        String bonusText = Integer.toString(treasureModel.getBasicValue()) + "/" + Integer.toString(treasureModel.getSpecialValue());
        
        lblBonus.setText(bonusText);
        
        String goldCoinsText = "$" + Integer.toString(treasureModel.getGoldCoins());

        lblGoldCoins.setText(goldCoinsText);

        lblTreasureKind.setText(treasureModel.getType().toString());
        
        lblImage.setIcon(new javax.swing.ImageIcon(getClass().getResource(treasureModel.getIcon())));
        
        //Forzamos una llamada al evento para que se cambie el color
        formMouseClicked(null);

        //Finalizar con la siguiente orden para que los cambios se hagan efectivos
        repaint();
    }

